package com.memo.studygroup.vo;

public class MemoVO {

	private int id;
	private String memo;
	private String date;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public String getRegdate() {
		return date;
	}

	public void setRegdate(String date) {
		this.date = date;
	}
}
